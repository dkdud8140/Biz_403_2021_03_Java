
package com.callor.values;

public class Values_05 {
	public static void main(String[] rags) {
		
		/*
		 * 코드에서 동등연산자(==)
		 * 어떤 값이 같은지 물어보는 연산자
		 * 값이 같으면 결과는 true
		 * 값이 다르면 결과는 false
		 */
		
		System.out.println(3 == 3);
		System.out.println(4 == 3);
		System.out.println(5 == 1);
		System.out.println();
		
		System.out.println(3 > 3);
		System.out.println(3 < 3);
		System.out.println();
		
		System.out.println(4 > 3);
		System.out.println(4 < 3);
		System.out.println();
		
		System.out.println(3 >= 3);
		System.out.println(3 <= 3);
		System.out.println();
		
		System.out.println(4 >= 3);
		System.out.println(4 <= 3);
		System.out.println();
		
		// != : 값이 서로 같지 아니한가?
		// 값이 서로 다르냐?		
		System.out.println(3 != 3);
		// = System.out.println(3 <> 3);
		
		
	}
}
