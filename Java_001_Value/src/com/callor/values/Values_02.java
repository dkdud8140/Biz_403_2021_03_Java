package com.callor.values;

public class Values_02 {
	public static void main(String[] args) {
		
		System.out.println("반지름이 23인 원의 둘레와 넓이를 계산하여 출력하여라");
		System.out.println("(단, 원의 둘레 = 반지름 x 2 x 3.14 / 원의 넓이 = 반지름 x 반지름 x 3.14)");
		
		System.out.println("------------------------------------------------------------------------------");
		
		System.out.print("원의 둘레 : ");
		System.out.print("23 x 2 x 3.14 = ");
		System.out.println(23 * 2 * 3.14);
		
		System.out.print("원의 넓이 : ");
		System.out.print("23 x 23 x 3.14 = ");
		System.out.println(23 * 23 * 3.14);
		
		
		
	}
	

}
