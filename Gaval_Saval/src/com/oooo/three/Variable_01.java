package com.oooo.three;

public class Variable_01 {
	public static void main(String[] args) {

		int num = 30;

		System.out.println(num + 40);
		System.out.println(num - 40);
		System.out.println(num * 40);
		System.out.println(num / 40);
		System.out.println(num % 40);

		System.out.println("***************");
		
		num = 70;

		System.out.println(num + 40);
		System.out.println(num - 40);
		System.out.println(num * 40);
		System.out.println(num / 40);
		System.out.println(num % 40);

	}
}
